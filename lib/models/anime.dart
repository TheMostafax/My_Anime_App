class Anime {
  final int id;
  final String title;
  final String image;
  final int episode;
  final String description;
  final double rating;
  final String genre;

  Anime({
    required this.id,
    required this.title,
    required this.image,
    required this.episode,
    required this.description,
    required this.rating,
    required this.genre,
  });
}
